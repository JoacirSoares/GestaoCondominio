    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(txtId.getText());
        UnidadeDAO unidadeDAO = new UnidadeDAO();
        unidadeDAO.excluir(id);

        txtId.setText("");
        txtIdentificacao.setText("");
        txtProprietario.setText("");
        txtCondominio.setText("");
        txtEndereco.setText("");
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSalvarEditacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarEditacaoActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(txtId.getText());
        String strIdentificacao = txtIdentificacao.getText();
        String strProprietario = txtProprietario.getText();
        String strCondominio = txtCondominio.getText();
        String strEndereco = txtEndereco.getText();

        Unidade unidade = new Unidade();
        unidade.setIdentificacao(strIdentificacao);
        unidade.setProprietario(strProprietario);
        unidade.setCondominio(strCondominio);
        unidade.setEndereco(strEndereco);

        UnidadeDAO unidadeDAO = new UnidadeDAO();
        unidadeDAO.editar(unidade, id);

        txtId.setText("");
        txtIdentificacao.setText("");
        txtProprietario.setText("");
        txtCondominio.setText("");
        txtEndereco.setText("");
    }//GEN-LAST:event_btnSalvarEditacaoActionPerformed

