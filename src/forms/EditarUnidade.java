    private void btnPesquisarIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarIdActionPerformed
        // TODO add your handling code here:
        int idPesquisa = Integer.parseInt(txtId.getText());
        UnidadeDAO unidadeDAO = new UnidadeDAO();
        Unidade unidade = unidadeDAO.getUnidade(idPesquisa);
        if(unidade == null) {
            JOptionPane.showMessageDialog(this, "Unidade não entrada");
        } else {
            // Preenche o form com os dados da despesa selecionada
            txtIdentificacao.setText(unidade.getIdentificacao());
            txtProprietario.setText(unidade.getProprietario());
            txtCondominio.setText(unidade.getCondominio());
            txtEndereco.setText(unidade.getEndereco());
        }
    }//GEN-LAST:event_btnPesquisarIdActionPerformed

