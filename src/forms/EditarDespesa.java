    private void btnPesquisarIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarIdActionPerformed
        // TODO add your handling code here:
        int idPesquisa = Integer.parseInt(txtId.getText());
        DespesaUnidadeDAO despesaUnidadeDAO = new DespesaUnidadeDAO();
        DespesaUnidade despesaUnidade = despesaUnidadeDAO.getDespesa(idPesquisa);
        if(despesaUnidade == null) {
            JOptionPane.showMessageDialog(this, "Despesa n√£o entrada");
        } else {
            // Preenche o form com os dados da despesa selecionada
            boxTipoDespesa.setSelectedItem(despesaUnidade.getTipoDespesa().toString());
            rodaValor.setValue(despesaUnidade.getValor());
            txtDescricao.setText(despesaUnidade.getDescricao());
            try {
                Date date = new SimpleDateFormat("yyyy-MM-dd").parse(despesaUnidade.getVencimentoFatura());
                jDateVencimento.setDate(date);
            } catch (ParseException ex) {
                Logger.getLogger(EditarDespesa.class.getName()).log(Level.SEVERE, null, ex);
            }
            boxStatus.setSelectedItem(despesaUnidade.getStatusPagamento().toString());
        }
    }//GEN-LAST:event_btnPesquisarIdActionPerformed

    private void btnExcluirDespesaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirDespesaActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(txtId.getText());
        DespesaUnidadeDAO despesaUnidadeDAO = new DespesaUnidadeDAO();
        despesaUnidadeDAO.excluir(id);
        
        txtId.setText("");
        boxTipoDespesa.setSelectedItem("AGUA");
        txtDescricao.setText("");
        rodaValor.setValue(0);
        boxStatus.setSelectedItem("PAGO");
    }//GEN-LAST:event_btnExcluirDespesaActionPerformed

