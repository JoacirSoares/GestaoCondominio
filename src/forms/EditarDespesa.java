    private void btnSalvarEditacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarEditacaoActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(txtId.getText());
        TipoDespesa tipo = TipoDespesa.valueOf(boxTipoDespesa.getSelectedItem().toString());
        String strDescicao = txtDescricao.getText();
        double rValor = Double.parseDouble(rodaValor.getValue().toString());
        SimpleDateFormat dFormat = new SimpleDateFormat("yyyy-MM-dd");
        String date = dFormat.format(jDateVencimento.getDate());
        StatusPagamento status = StatusPagamento.valueOf(boxStatus.getSelectedItem().toString());

        DespesaUnidade despesaUnidade = new DespesaUnidade();
        despesaUnidade.setTipoDespesa(tipo);
        despesaUnidade.setDescricao(strDescicao);
        despesaUnidade.setValor(rValor);
        despesaUnidade.setVencimentoFatura(date);
        despesaUnidade.setStatusPagamento(status);

        DespesaUnidadeDAO despesaUnidadeDAO = new DespesaUnidadeDAO();
        despesaUnidadeDAO.editar(despesaUnidade, id);

        txtId.setText("");
        boxTipoDespesa.setSelectedItem("AGUA");
        txtDescricao.setText("");
        rodaValor.setValue(0);
        boxStatus.setSelectedItem("PAGO");
    }//GEN-LAST:event_btnSalvarEditacaoActionPerformed

